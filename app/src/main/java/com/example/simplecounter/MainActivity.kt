@Composable
fun Counter(){
    var number = remember { mutableStateOf(1000) }



    Column (
        modifier = Modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ){
        Text(
            text = number.value.toString(),
            fontSize = 24.sp
        )

        Button(onClick = {
            println("clicked")
            number.value--
        }) {
            Text("Click me")
        }

    }
}




